function local_save_case_result(runDir, row, cand, spec, results, elapsedSec, saveFullResults)
caseResult = struct();
caseResult.row = row;
caseResult.candidate = cand;
caseResult.txSummary = struct("burstDurationSec", double(results.tx.burstDurationSec));
caseResult.elapsedSec = elapsedSec;
if saveFullResults
    save(fullfile(runDir, "case_result.mat"), "caseResult", "cand", "spec", "results", "elapsedSec", "-v7.3");
else
    save(fullfile(runDir, "case_result.mat"), "caseResult", "cand", "spec");
end
end

