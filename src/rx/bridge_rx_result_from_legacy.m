function rxResults = bridge_rx_result_from_legacy(profileName, legacyResults, roleName, rxChainName)
%BRIDGE_RX_RESULT_FROM_LEGACY Convert legacy results into standardized rxResult entries.

arguments
    profileName (1,1) string
    legacyResults (1,1) struct
    roleName (1,1) string
    rxChainName (1,1) string
end

methods = string(legacyResults.methods(:).');
if roleName == "bob"
    diag = legacyResults.packetDiagnostics.bob;
    per = legacyResults.per;
    rawPer = legacyResults.rawPer;
    ber = legacyResults.ber;
else
    if ~(isfield(legacyResults, "eve") && isstruct(legacyResults.eve) ...
            && isfield(legacyResults.eve, "packetDiagnostics"))
        error("bridge_rx_result_from_legacy requires legacyResults.eve.packetDiagnostics for role eve.");
    end
    diag = legacyResults.eve.packetDiagnostics;
    per = legacyResults.eve.per;
    rawPer = legacyResults.eve.rawPer;
    ber = legacyResults.eve.ber;
end

nMethods = numel(methods);
rxResults = repmat(struct( ...
    "method", "", ...
    "frontEndOk", false(1, 0), ...
    "headerOk", false(1, 0), ...
    "packetOk", false(1, 0), ...
    "rawPacketOk", false(1, 0), ...
    "payloadBits", uint8([]), ...
    "metrics", struct(), ...
    "commonDiagnostics", struct(), ...
    "profileDiagnostics", struct()), nMethods, 1);

for idx = 1:nMethods
    rxResults(idx).method = methods(idx);
    rxResults(idx).frontEndOk = double(diag.frontEndSuccessRateByMethod(idx, :)) >= 1 - 1e-12;
    rxResults(idx).headerOk = double(diag.headerSuccessRateByMethod(idx, :)) >= 1 - 1e-12;
    rxResults(idx).packetOk = double(1 - per(idx, :)) >= 1 - 1e-12;
    rxResults(idx).rawPacketOk = double(1 - rawPer(idx, :)) >= 1 - 1e-12;
    rxResults(idx).payloadBits = uint8([]);
    rxResults(idx).metrics = struct( ...
        "ebN0dB", legacyResults.ebN0dB, ...
        "jsrDb", legacyResults.jsrDb, ...
        "ber", ber(idx, :), ...
        "rawPer", rawPer(idx, :), ...
        "per", per(idx, :));
    rxResults(idx).commonDiagnostics = struct( ...
        "frontEndSuccessRate", diag.frontEndSuccessRateByMethod(idx, :), ...
        "headerSuccessRate", diag.headerSuccessRateByMethod(idx, :));
    rxResults(idx).profileDiagnostics = struct( ...
        "profileName", profileName, ...
        "receiver", rxChainName, ...
        "backend", "legacy_bridge", ...
        "role", roleName);
end
end
