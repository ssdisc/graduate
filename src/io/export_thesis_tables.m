    error("export_thesis_tables:InvalidChannelConfig", ...
        "%s.multipath.pathDelaysSymbols and %s.multipath.pathGainsDb must have the same length.", ...
        structName, structName);
end

channelCfg = struct( ...
    "impulseProb", local_require_finite_scalar(channel.impulseProb, structName + ".impulseProb"), ...
    "impulseToBgRatio", local_require_finite_scalar(channel.impulseToBgRatio, structName + ".impulseToBgRatio"), ...
    "impulseWeight", local_require_finite_scalar(channel.impulseWeight, structName + ".impulseWeight"), ...
    "singleToneEnable", local_require_logical_scalar(channel.singleTone.enable, structName + ".singleTone.enable"), ...
    "singleToneWeight", local_require_finite_scalar(channel.singleTone.weight, structName + ".singleTone.weight"), ...
    "singleToneFreqHz", local_require_finite_scalar(channel.singleTone.freqHz, structName + ".singleTone.freqHz"), ...
    "singleToneRandomPhase", local_require_logical_scalar(channel.singleTone.randomPhase, structName + ".singleTone.randomPhase"), ...
    "narrowbandEnable", local_require_logical_scalar(channel.narrowband.enable, structName + ".narrowband.enable"), ...
    "narrowbandWeight", local_require_finite_scalar(channel.narrowband.weight, structName + ".narrowband.weight"), ...
    "narrowbandCenterFreqPoints", local_require_finite_scalar(channel.narrowband.centerFreqPoints, structName + ".narrowband.centerFreqPoints"), ...
    "narrowbandBandwidthFreqPoints", local_require_finite_scalar(channel.narrowband.bandwidthFreqPoints, structName + ".narrowband.bandwidthFreqPoints"), ...
    "sweepEnable", local_require_logical_scalar(channel.sweep.enable, structName + ".sweep.enable"), ...
    "sweepWeight", local_require_finite_scalar(channel.sweep.weight, structName + ".sweep.weight"), ...
    "sweepStartHz", local_require_finite_scalar(channel.sweep.startHz, structName + ".sweep.startHz"), ...
    "sweepStopHz", local_require_finite_scalar(channel.sweep.stopHz, structName + ".sweep.stopHz"), ...
    "sweepPeriodSymbols", local_require_finite_scalar(channel.sweep.periodSymbols, structName + ".sweep.periodSymbols"), ...
    "sweepRandomPhase", local_require_logical_scalar(channel.sweep.randomPhase, structName + ".sweep.randomPhase"), ...
    "syncImpairmentEnable", local_require_logical_scalar(channel.syncImpairment.enable, structName + ".syncImpairment.enable"), ...
    "syncTimingOffsetSymbols", local_require_finite_scalar(channel.syncImpairment.timingOffsetSymbols, structName + ".syncImpairment.timingOffsetSymbols"), ...
    "syncPhaseOffsetRad", local_require_finite_scalar(channel.syncImpairment.phaseOffsetRad, structName + ".syncImpairment.phaseOffsetRad"), ...
    "multipathEnable", local_require_logical_scalar(channel.multipath.enable, structName + ".multipath.enable"), ...
    "multipathPathDelaysSymbols", local_join_numeric_vector(pathDelaysSymbols), ...
    "multipathPathGainsDb", local_join_numeric_vector(pathGainsDb), ...
    "multipathRayleigh", local_require_logical_scalar(channel.multipath.rayleigh, structName + ".multipath.rayleigh"));
end

function rxCfg = local_require_rx_diversity_config(rxDiv, structName)
local_require_fields(rxDiv, ["enable" "nRx" "combineMethod"], structName);
rxCfg = struct( ...
    "enable", local_require_logical_scalar(rxDiv.enable, structName + ".enable"), ...
    "nRx", local_require_finite_scalar(rxDiv.nRx, structName + ".nRx"), ...
    "combineMethod", string(rxDiv.combineMethod));
if ~(isscalar(rxCfg.nRx) && isfinite(rxCfg.nRx) && rxCfg.nRx >= 1 && abs(rxCfg.nRx - round(rxCfg.nRx)) <= 1e-12)
    error("export_thesis_tables:InvalidRxDiversity", "%s.nRx must be a positive integer scalar.", structName);
end
rxCfg.nRx = round(rxCfg.nRx);
if strlength(rxCfg.combineMethod) == 0
    error("export_thesis_tables:InvalidRxDiversity", "%s.combineMethod must be a non-empty string scalar.", structName);
end
end

function [enabledTypes, enabledCount] = local_collect_enabled_interference_types(channelCfg)
enabledTypes = strings(1, 0);

if channelCfg.impulseWeight > 0
    if channelCfg.impulseProb <= 0
        error("export_thesis_tables:InvalidChannelConfig", ...
            "channelImpulseWeight>0 时，channelImpulseProb 必须为正。");
    end
    enabledTypes(end + 1) = "impulse"; %#ok<AGROW>
end

if channelCfg.singleToneEnable
    if channelCfg.singleToneWeight <= 0
        error("export_thesis_tables:InvalidChannelConfig", ...
            "channelSingleToneEnable=true 时，channelSingleToneWeight 必须为正。");
    end
    enabledTypes(end + 1) = "singleTone"; %#ok<AGROW>
end

if channelCfg.narrowbandEnable
    if channelCfg.narrowbandWeight <= 0
        error("export_thesis_tables:InvalidChannelConfig", ...
            "channelNarrowbandEnable=true 时，channelNarrowbandWeight 必须为正。");
    end
    enabledTypes(end + 1) = "narrowband"; %#ok<AGROW>
end

if channelCfg.sweepEnable
    if channelCfg.sweepWeight <= 0
        error("export_thesis_tables:InvalidChannelConfig", ...
            "channelSweepEnable=true 时，channelSweepWeight 必须为正。");
    end
    enabledTypes(end + 1) = "sweep"; %#ok<AGROW>
end

enabledCount = numel(enabledTypes);
if enabledCount == 0
    enabledTypes = "none";
end
end

