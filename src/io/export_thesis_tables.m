function values = local_require_numeric_vector(valuesIn, nPoints, fieldName)
values = double(valuesIn(:));
if numel(values) ~= nPoints || any(~isfinite(values))
    error("export_thesis_tables:InvalidNumericVector", ...
        "%s must be a finite vector with %d elements.", fieldName, nPoints);
end
end

function value = local_require_finite_scalar(valueIn, fieldName)
value = double(valueIn);
if ~isscalar(value) || ~isfinite(value)
    error("export_thesis_tables:InvalidScalar", ...
        "%s must be a finite scalar.", fieldName);
end
end

function value = local_require_logical_scalar(valueIn, fieldName)
if ~isscalar(valueIn) || ~(islogical(valueIn) || isnumeric(valueIn))
    error("export_thesis_tables:InvalidLogicalScalar", ...
        "%s must be a logical scalar.", fieldName);
end
value = logical(valueIn);
end

function values = local_require_numeric_list(valuesIn, fieldName)
values = double(valuesIn(:).');
if isempty(values) || any(~isfinite(values))
    error("export_thesis_tables:InvalidNumericList", ...
        "%s must be a nonempty finite numeric vector.", fieldName);
end
end

function local_require_fields(s, fieldNames, structName)
if nargin < 3
    structName = "struct";
end
fieldNames = string(fieldNames);
for i = 1:numel(fieldNames)
    fieldName = fieldNames(i);
    if ~isfield(s, fieldName)
        error("export_thesis_tables:MissingField", ...
            "Missing required field %s.%s.", structName, fieldName);
    end
end
end

function txt = local_join_string_vector(values)
values = string(values(:).');
txt = join(values, ",");
txt = txt(1);
end

function txt = local_join_numeric_vector(values)
values = double(values(:).');
parts = compose("%.6g", values);
txt = join(parts, ",");
txt = txt(1);
end
