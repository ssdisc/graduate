    ldpcRateName, ...
    ldpcFrameType, ...
    ldpcInfoBits, ...
    ldpcBlockLength, ...
    ldpcMaxIterations, ...
    string(results.params.payload.codec), ...
    string(results.params.frame.sessionHeaderMode), ...
    logical(results.params.dsss.enable), ...
    double(results.params.dsss.spreadFactor), ...
    string(results.params.dsss.sequenceType), ...
    local_join_numeric_vector(double(results.params.dsss.pnPolynomial)), ...
    local_join_numeric_vector(double(results.params.dsss.pnInit)), ...
    logical(results.params.fh.enable), ...
    logical(rxDivCfg.enable), ...
    double(rxDivCfg.nRx), ...
    string(rxDivCfg.combineMethod), ...
    logical(packetConcealActive), ...
    string(packetConcealMode), ...
    string(scan.type), ...
    local_join_numeric_vector(scan.ebN0dBList), ...
    local_join_numeric_vector(scan.jsrDbList), ...
    double(scan.nSnr), ...
    double(scan.nJsr), ...
    local_join_string_vector(methods), ...
    nMethods, ...
    nPoints, ...
    local_join_numeric_vector(results.tx.configuredPowerLin), ...
    local_join_numeric_vector(results.tx.configuredPowerDb), ...
    double(results.linkBudget.noisePsdLin), ...
    local_join_string_vector(enabledInterferenceTypes), ...
    enabledInterferenceCount, ...
    channelCfg.impulseProb, ...
    channelCfg.impulseToBgRatio, ...
    channelCfg.impulseWeight, ...
    channelCfg.singleToneEnable, ...
    channelCfg.singleToneWeight, ...
    channelCfg.singleToneFreqHz, ...
    channelCfg.singleToneRandomPhase, ...
    channelCfg.narrowbandEnable, ...
    channelCfg.narrowbandWeight, ...
    channelCfg.narrowbandCenterFreqPoints, ...
    channelCfg.narrowbandBandwidthFreqPoints, ...
    channelCfg.sweepEnable, ...
    channelCfg.sweepWeight, ...
    channelCfg.sweepStartHz, ...
    channelCfg.sweepStopHz, ...
    channelCfg.sweepPeriodSymbols, ...
    channelCfg.sweepRandomPhase, ...
    channelCfg.syncImpairmentEnable, ...
    channelCfg.syncTimingOffsetSymbols, ...
    channelCfg.syncPhaseOffsetRad, ...
    channelCfg.multipathEnable, ...
    channelCfg.multipathPathDelaysSymbols, ...
    channelCfg.multipathPathGainsDb, ...
