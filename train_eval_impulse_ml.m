function combinedReport = train_eval_impulse_ml(varargin)
%TRAIN_EVAL_IMPULSE_ML Retrain the refactored impulse-profile ML front-end and compare it.

opts = local_parse_inputs(varargin{:});

addpath(genpath(fullfile(fileparts(mfilename("fullpath")), "src")));

tag = string(opts.Tag);
outRoot = fullfile(char(opts.ResultsRoot), "impulse_ml_train_eval", char(tag));
if ~exist(outRoot, "dir")
    mkdir(outRoot);
end
modelDir = fullfile(pwd, "models");
if ~exist(modelDir, "dir")
    mkdir(modelDir);
end

fprintf("[IMPULSE-ML] Output root: %s\n", outRoot);
fprintf("[IMPULSE-ML] Model dir: %s\n", modelDir);

linkSpec = default_params( ...
    "linkProfileName", "impulse", ...
    "strictModelLoad", false, ...
    "requireTrainedMlModels", false, ...
    "loadMlModels", strings(1, 0));
linkSpec.sim.saveFigures = false;
linkSpec.sim.useParallel = false;
pTrain = compile_runtime_config(linkSpec);

trainArgs = local_training_args(opts);
needGru = logical(opts.TrainGru) || any(contains(lower(opts.CompareMethods), "ml_gru"));

fprintf("[IMPULSE-ML] Training CNN on refactored impulse profile: nBlocks=%d blockLen=%d epochs=%d\n", ...
    opts.CnnBlocks, opts.BlockLen, opts.CnnEpochs);
tic;
[cnnModel, cnnReport] = ml_train_cnn_impulse(pTrain, ...
    trainArgs{:}, ...
    "nBlocks", opts.CnnBlocks, ...
    "blockLen", opts.BlockLen, ...
    "epochs", opts.CnnEpochs, ...
    "batchSize", opts.BatchSize, ...
    "lr", opts.LearnRate, ...
    "rngSeed", opts.RngSeed, ...
    "useGpu", opts.UseGpu, ...
    "saveArtifacts", true, ...
    "saveDir", string(modelDir), ...
    "saveTag", tag, ...
    "savedBy", "train_eval_impulse_ml", ...
    "verbose", true);
cnnTrainElapsed = toc;
fprintf("[IMPULSE-ML] CNN trained in %.2fs, threshold=%.4f, testPe=%.4f\n", ...
    cnnTrainElapsed, double(cnnModel.threshold), double(cnnReport.test.pe));

gruModel = struct();
gruReport = struct("artifacts", struct("batchPath", string(fullfile(modelDir, "__skip_gru__.mat"))));
gruTrainElapsed = NaN;
if needGru
    fprintf("[IMPULSE-ML] Training GRU on refactored impulse profile: nBlocks=%d blockLen=%d epochs=%d\n", ...
        opts.GruBlocks, opts.BlockLen, opts.GruEpochs);
    tic;
    [gruModel, gruReport] = ml_train_gru_impulse(pTrain, ...
        trainArgs{:}, ...
        "nBlocks", opts.GruBlocks, ...
        "blockLen", opts.BlockLen, ...
        "epochs", opts.GruEpochs, ...
        "batchSize", opts.BatchSize, ...
        "lr", opts.LearnRate, ...
        "rngSeed", opts.RngSeed + 17, ...
        "useGpu", opts.UseGpu, ...
        "saveArtifacts", true, ...
        "saveDir", string(modelDir), ...
        "saveTag", tag, ...
        "savedBy", "train_eval_impulse_ml", ...
        "verbose", true);
    gruTrainElapsed = toc;
    fprintf("[IMPULSE-ML] GRU trained in %.2fs, threshold=%.4f, testPe=%.4f\n", ...
        gruTrainElapsed, double(gruModel.threshold), double(gruReport.test.pe));
else
    fprintf("[IMPULSE-ML] Skipping GRU training for this run.\n");
end

reportsPath = fullfile(outRoot, "training_reports.mat");
save(reportsPath, "cnnModel", "cnnReport", "cnnTrainElapsed", ...
    "gruModel", "gruReport", "gruTrainElapsed", "opts");
fprintf("[IMPULSE-ML] Training reports saved: %s\n", reportsPath);

compareTag = tag + "_compare";
compareReport = validate_impulse_ml_compare( ...
    "ImpulseProb", opts.ImpulseProbList, ...
    "Methods", opts.CompareMethods, ...
    "EbN0", opts.EbN0, ...
    "JsrDb", opts.JsrDb, ...
    "NFrames", opts.NFramesPerPoint, ...
    "MaxBurstSec", opts.MaxBurstSec, ...
    "MaxElapsedSec", opts.MaxElapsedSec, ...
    "CnnModelPath", cnnReport.artifacts.batchPath, ...
    "GruModelPath", gruReport.artifacts.batchPath, ...
    "RequireMl", true, ...
    "ResultsRoot", outRoot, ...
    "Tag", compareTag);

combinedReport = struct();
combinedReport.generatedAt = string(datetime("now", "Format", "yyyy-MM-dd HH:mm:ss"));
combinedReport.tag = tag;
combinedReport.outRoot = string(outRoot);
combinedReport.opts = opts;
combinedReport.training = struct( ...
    "cnnElapsedSec", cnnTrainElapsed, ...
    "gruElapsedSec", gruTrainElapsed, ...
    "cnnModelPath", string(cnnReport.artifacts.batchPath), ...
    "gruModelPath", string(gruReport.artifacts.batchPath), ...
    "cnnReport", cnnReport, ...
    "gruReport", gruReport);
combinedReport.compare = compareReport;

save(fullfile(outRoot, "combined_report.mat"), "combinedReport");
local_print_compare_summary(compareReport.summaryTable);
fprintf("[IMPULSE-ML] Combined report saved: %s\n", fullfile(outRoot, "combined_report.mat"));
end

function opts = local_parse_inputs(varargin)
p = inputParser();
p.FunctionName = "train_eval_impulse_ml";
addParameter(p, "Tag", string(datetime("now", "Format", "yyyyMMdd_HHmmss")), @(x) ischar(x) || isstring(x));
addParameter(p, "ResultsRoot", fullfile(pwd, "results"), @(x) ischar(x) || isstring(x));
addParameter(p, "RngSeed", 20260426, @(x) isscalar(x) && isnumeric(x) && isfinite(x));
addParameter(p, "UseGpu", true, @(x) islogical(x) || isnumeric(x));
addParameter(p, "TrainGru", false, @(x) islogical(x) || isnumeric(x));
addParameter(p, "CnnBlocks", 384, @(x) isscalar(x) && isnumeric(x) && x >= 16);
addParameter(p, "GruBlocks", 256, @(x) isscalar(x) && isnumeric(x) && x >= 16);
addParameter(p, "BlockLen", 1024, @(x) isscalar(x) && isnumeric(x) && x >= 128);
addParameter(p, "CnnEpochs", 16, @(x) isscalar(x) && isnumeric(x) && x >= 1);
addParameter(p, "GruEpochs", 12, @(x) isscalar(x) && isnumeric(x) && x >= 1);
addParameter(p, "BatchSize", 32, @(x) isscalar(x) && isnumeric(x) && x >= 1);
addParameter(p, "LearnRate", 1e-3, @(x) isscalar(x) && isnumeric(x) && x > 0);
addParameter(p, "TrainEbN0dBRange", [4 8], @(x) isnumeric(x) && isvector(x) && numel(x) == 2);
addParameter(p, "TrainJsrDbRange", [-2 2], @(x) isnumeric(x) && isvector(x) && numel(x) == 2);
addParameter(p, "TrainImpulseProbRange", [0.03 0.18], @(x) isnumeric(x) && isvector(x) && numel(x) == 2);
addParameter(p, "TrainImpulseProbFocusRange", [0.08 0.18], @(x) isnumeric(x) && isvector(x) && numel(x) == 2);
addParameter(p, "TrainImpulseProbFocusProbability", 0.75, @(x) isscalar(x) && isnumeric(x) && isfinite(x) && x >= 0 && x <= 1);
addParameter(p, "EbN0", 6, @(x) isscalar(x) && isnumeric(x) && isfinite(x));
addParameter(p, "JsrDb", 0, @(x) isscalar(x) && isnumeric(x) && isfinite(x));
addParameter(p, "NFramesPerPoint", 1, @(x) isscalar(x) && isnumeric(x) && x >= 1);
addParameter(p, "ImpulseProbList", [0.01 0.03 0.05 0.08 0.12 0.16], @(x) isnumeric(x) && isvector(x) && ~isempty(x));
addParameter(p, "CompareMethods", ["none" "blanking" "clipping" "ml_cnn"], @(x) isstring(x) || ischar(x) || iscellstr(x));
addParameter(p, "MaxBurstSec", 60, @(x) isscalar(x) && isnumeric(x) && isfinite(x) && x > 0);
addParameter(p, "MaxElapsedSec", 60, @(x) isscalar(x) && isnumeric(x) && isfinite(x) && x > 0);
parse(p, varargin{:});
opts = p.Results;
opts.Tag = string(opts.Tag);
opts.ResultsRoot = string(opts.ResultsRoot);
opts.UseGpu = logical(opts.UseGpu);
opts.TrainGru = logical(opts.TrainGru);
opts.CnnBlocks = round(double(opts.CnnBlocks));
opts.GruBlocks = round(double(opts.GruBlocks));
opts.BlockLen = round(double(opts.BlockLen));
opts.CnnEpochs = round(double(opts.CnnEpochs));
opts.GruEpochs = round(double(opts.GruEpochs));
opts.BatchSize = round(double(opts.BatchSize));
opts.NFramesPerPoint = round(double(opts.NFramesPerPoint));
opts.CompareMethods = unique(string(opts.CompareMethods(:).'), "stable");
end

function args = local_training_args(opts)
args = { ...
    "ebN0dBRange", double(opts.TrainEbN0dBRange), ...
    "labelScoreThreshold", 0.1, ...
    "pfaTarget", 0.05, ...
    "thresholdPolicy", "max_fbeta_under_pfa", ...
    "thresholdBeta", 4.0, ...
    "thresholdMaxCandidates", 129, ...
    "thresholdEvalFramesPerPoint", 1, ...
    "thresholdEvalEbN0dBList", double(opts.EbN0), ...
    "thresholdEvalJsrDbList", double(opts.JsrDb), ...
    "minPositiveRate", 0.002, ...
    "maxPositiveRate", 0.45, ...
    "impulsePowerMode", "jsr_calibrated", ...
    "jsrDbRange", double(opts.TrainJsrDbRange), ...
    "impulseEnableProbability", 1.0, ...
    "impulseProbRange", double(opts.TrainImpulseProbRange), ...
    "impulseProbFocusRange", double(opts.TrainImpulseProbFocusRange), ...
    "impulseProbFocusProbability", double(opts.TrainImpulseProbFocusProbability), ...
    "singleToneProbability", 0.0, ...
    "narrowbandProbability", 0.0, ...
    "sweepProbability", 0.0, ...
    "syncImpairmentProbability", 0.0, ...
    "multipathProbability", 0.0, ...
    "maxAdditionalImpairments", 0};
end

function local_print_compare_summary(tbl)
if isempty(tbl)
    fprintf("[IMPULSE-ML] No comparison rows.\n");
    return;
end

methods = string(unique(tbl.method, "stable")).';
fprintf("\n[IMPULSE-ML] Compare summary by method\n");
for method = methods
    mask = tbl.runOk & tbl.method == method;
    sub = tbl(mask, :);
    if isempty(sub)
        fprintf("  %-10s no valid rows\n", char(method));
        continue;
    end
    fprintf("  %-10s pass=%d/%d maxPER=%.4g maxRawPER=%.4g burstMax=%.3fs elapsedMax=%.2fs\n", ...
        char(method), nnz(sub.pass), height(sub), ...
        max(sub.per, [], 'omitnan'), max(sub.rawPer, [], 'omitnan'), ...
        max(sub.burstSec, [], 'omitnan'), max(sub.elapsedSec, [], 'omitnan'));
end
end
