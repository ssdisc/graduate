function combinedSummary = train_eval_impulse_ml(varargin)
%TRAIN_EVAL_IMPULSE_ML Train CNN/GRU impulse suppressors and evaluate the impulse link.

opts = local_parse_inputs(varargin{:});

addpath(genpath(fullfile(fileparts(mfilename("fullpath")), "src")));

tag = string(opts.Tag);
outRoot = fullfile(char(opts.ResultsRoot), "impulse_ml_train_eval", char(tag));
if ~exist(outRoot, "dir")
    mkdir(outRoot);
end
modelDir = fullfile(pwd, "models");
if ~exist(modelDir, "dir")
    mkdir(modelDir);
end

fprintf("[IMPULSE-ML] Output root: %s\n", outRoot);
fprintf("[IMPULSE-ML] Model dir: %s\n", modelDir);

linkSpec = default_params( ...
    "linkProfileName", "impulse", ...
    "strictModelLoad", false, ...
    "requireTrainedMlModels", false, ...
    "loadMlModels", strings(1, 0));
linkSpec.sim.saveFigures = false;
linkSpec.sim.useParallel = false;
pTrain = compile_runtime_config(linkSpec);

trainArgs = local_training_args(opts);

fprintf("[IMPULSE-ML] Training CNN: nBlocks=%d blockLen=%d epochs=%d\n", ...
    opts.CnnBlocks, opts.BlockLen, opts.CnnEpochs);
tic;
[cnnModel, cnnReport] = ml_train_cnn_impulse(pTrain, ...
    trainArgs{:}, ...
    "nBlocks", opts.CnnBlocks, ...
    "blockLen", opts.BlockLen, ...
    "epochs", opts.CnnEpochs, ...
    "batchSize", opts.BatchSize, ...
    "lr", opts.LearnRate, ...
    "rngSeed", opts.RngSeed, ...
    "useGpu", opts.UseGpu, ...
    "saveArtifacts", true, ...
    "saveDir", string(modelDir), ...
    "saveTag", tag, ...
    "savedBy", "train_eval_impulse_ml", ...
    "verbose", true);
cnnTrainElapsed = toc;
fprintf("[IMPULSE-ML] CNN trained in %.2fs, threshold=%.4f\n", ...
    cnnTrainElapsed, double(cnnModel.threshold));

fprintf("[IMPULSE-ML] Training GRU: nBlocks=%d blockLen=%d epochs=%d\n", ...
    opts.GruBlocks, opts.BlockLen, opts.GruEpochs);
tic;
[gruModel, gruReport] = ml_train_gru_impulse(pTrain, ...
    trainArgs{:}, ...
    "nBlocks", opts.GruBlocks, ...
    "blockLen", opts.BlockLen, ...
    "epochs", opts.GruEpochs, ...
    "batchSize", opts.BatchSize, ...
    "lr", opts.LearnRate, ...
    "rngSeed", opts.RngSeed + 17, ...
    "useGpu", opts.UseGpu, ...
    "saveArtifacts", true, ...
    "saveDir", string(modelDir), ...
    "saveTag", tag, ...
    "savedBy", "train_eval_impulse_ml", ...
    "verbose", true);
gruTrainElapsed = toc;
fprintf("[IMPULSE-ML] GRU trained in %.2fs, threshold=%.4f\n", ...
    gruTrainElapsed, double(gruModel.threshold));

reportsPath = fullfile(outRoot, "training_reports.mat");
save(reportsPath, "cnnModel", "cnnReport", "cnnTrainElapsed", ...
    "gruModel", "gruReport", "gruTrainElapsed", "opts");
fprintf("[IMPULSE-ML] Training reports saved: %s\n", reportsPath);

combinedSummary = table();
scenarioList = local_eval_scenarios(opts);
for scenarioIdx = 1:numel(scenarioList)
    scenario = scenarioList(scenarioIdx);
    methods = string(scenario.methods(:).');
    for methodIdx = 1:numel(methods)
        methodName = methods(methodIdx);
        fprintf("[IMPULSE-ML] Evaluating scenario=%s method=%s\n", ...
            char(scenario.name), char(methodName));
        tic;
        tbl = scan_impulse_cases( ...
            "ImpulseProbList", opts.ImpulseProbList, ...
            "ImpulseToBgRatioList", opts.ImpulseToBgRatioList, ...
            "EbN0", opts.EbN0, ...
            "JsrDb", opts.JsrDb, ...
            "NFramesPerPoint", opts.NFramesPerPoint, ...
            "Method", methodName, ...
            "LoadMlModels", local_load_models_for_method(methodName), ...
            "RequireTrainedMlModels", startsWith(methodName, "ml_"), ...
            "RsK", scenario.rsK, ...
            "RsP", scenario.rsP, ...
            "SaveFigures", false, ...
            "ResultsRoot", outRoot, ...
            "Tag", tag + "_" + scenario.name + "_" + methodName);
        elapsed = toc;
        tbl.scenario = repmat(string(scenario.name), height(tbl), 1);
        tbl.evalElapsedSec = repmat(elapsed, height(tbl), 1);
        tbl.passPer0 = tbl.runOk & tbl.per <= 1e-12;
        tbl.outerRsK = repmat(double(scenario.rsK), height(tbl), 1);
        tbl.outerRsP = repmat(double(scenario.rsP), height(tbl), 1);
        combinedSummary = [combinedSummary; tbl]; %#ok<AGROW>
    end
end

summaryCsv = fullfile(outRoot, "impulse_ml_combined_summary.csv");
writetable(combinedSummary, summaryCsv);
local_print_summary(combinedSummary);
fprintf("[IMPULSE-ML] Combined summary written: %s\n", summaryCsv);
end

function opts = local_parse_inputs(varargin)
p = inputParser();
p.FunctionName = "train_eval_impulse_ml";
addParameter(p, "Tag", string(datetime("now", "Format", "yyyyMMdd_HHmmss")), @(x) ischar(x) || isstring(x));
addParameter(p, "ResultsRoot", fullfile(pwd, "results"), @(x) ischar(x) || isstring(x));
addParameter(p, "RngSeed", 20260426, @(x) isscalar(x) && isnumeric(x) && isfinite(x));
addParameter(p, "UseGpu", false, @(x) islogical(x) || isnumeric(x));
addParameter(p, "CnnBlocks", 192, @(x) isscalar(x) && isnumeric(x) && x >= 8);
addParameter(p, "GruBlocks", 160, @(x) isscalar(x) && isnumeric(x) && x >= 8);
addParameter(p, "BlockLen", 512, @(x) isscalar(x) && isnumeric(x) && x >= 64);
addParameter(p, "CnnEpochs", 8, @(x) isscalar(x) && isnumeric(x) && x >= 1);
addParameter(p, "GruEpochs", 6, @(x) isscalar(x) && isnumeric(x) && x >= 1);
addParameter(p, "BatchSize", 32, @(x) isscalar(x) && isnumeric(x) && x >= 1);
addParameter(p, "LearnRate", 1e-3, @(x) isscalar(x) && isnumeric(x) && x > 0);
addParameter(p, "EbN0", 6, @(x) isscalar(x) && isnumeric(x) && isfinite(x));
addParameter(p, "JsrDb", 0, @(x) isscalar(x) && isnumeric(x) && isfinite(x));
addParameter(p, "NFramesPerPoint", 1, @(x) isscalar(x) && isnumeric(x) && x >= 1);
addParameter(p, "ImpulseProbList", [0.01 0.03 0.05], @(x) isnumeric(x) && isvector(x) && ~isempty(x));
addParameter(p, "ImpulseToBgRatioList", [20 50 80], @(x) isnumeric(x) && isvector(x) && ~isempty(x));
addParameter(p, "RunLightRs", true, @(x) islogical(x) || isnumeric(x));
parse(p, varargin{:});
opts = p.Results;
opts.Tag = string(opts.Tag);
opts.ResultsRoot = string(opts.ResultsRoot);
opts.UseGpu = logical(opts.UseGpu);
opts.CnnBlocks = round(double(opts.CnnBlocks));
opts.GruBlocks = round(double(opts.GruBlocks));
opts.BlockLen = round(double(opts.BlockLen));
opts.CnnEpochs = round(double(opts.CnnEpochs));
opts.GruEpochs = round(double(opts.GruEpochs));
opts.BatchSize = round(double(opts.BatchSize));
opts.NFramesPerPoint = round(double(opts.NFramesPerPoint));
opts.RunLightRs = logical(opts.RunLightRs);
end

function args = local_training_args(opts)
args = { ...
    "ebN0dBRange", [0 10], ...
    "labelScoreThreshold", 0.1, ...
    "pfaTarget", 0.02, ...
    "thresholdPolicy", "min_pe_under_pfa", ...
    "thresholdMaxCandidates", 129, ...
    "thresholdEvalFramesPerPoint", 1, ...
    "thresholdEvalEbN0dBList", opts.EbN0, ...
    "thresholdEvalJsrDbList", opts.JsrDb, ...
    "minPositiveRate", 0.001, ...
    "maxPositiveRate", 0.40, ...
    "impulseEnableProbability", 1.0, ...
    "impulseProbRange", [0.005 0.06], ...
    "impulseToBgRatioRange", [10 100], ...
    "singleToneProbability", 0.0, ...
    "narrowbandProbability", 0.0, ...
    "sweepProbability", 0.0, ...
    "syncImpairmentProbability", 0.0, ...
    "multipathProbability", 0.0, ...
    "maxAdditionalImpairments", 0};
end

function scenarioList = local_eval_scenarios(opts)
scenarioList = struct("name", "default_rs", "rsK", 4, "rsP", 4, ...
    "methods", ["blanking" "clipping" "ml_cnn" "ml_gru"]);
if opts.RunLightRs
    scenarioList(end + 1) = struct("name", "light_rs", "rsK", 6, "rsP", 2, ...
        "methods", ["clipping" "ml_cnn"]);
end
end

function loadModels = local_load_models_for_method(methodName)
methodName = lower(string(methodName));
switch methodName
    case {"ml_cnn", "ml_cnn_hard"}
        loadModels = "cnn";
    case {"ml_gru", "ml_gru_hard"}
        loadModels = "gru";
    case "ml_blanking"
        loadModels = "lr";
    otherwise
        loadModels = strings(1, 0);
end
end

function local_print_summary(tbl)
if isempty(tbl)
    fprintf("[IMPULSE-ML] No evaluation rows.\n");
    return;
end
groups = unique(tbl(:, ["scenario", "methodLabel"]), "rows");
fprintf("\n[IMPULSE-ML] Aggregate by scenario/method\n");
for k = 1:height(groups)
    mask = tbl.scenario == groups.scenario(k) & tbl.methodLabel == groups.methodLabel(k);
    sub = tbl(mask, :);
    fprintf("  %-10s %-10s pass=%d/%d maxPER=%.4g maxRawPER=%.4g burstMax=%.3fs eval=%.2fs\n", ...
        char(groups.scenario(k)), char(groups.methodLabel(k)), ...
        nnz(sub.passPer0), height(sub), max(sub.per), max(sub.rawPer), ...
        max(sub.burstDurationSec), max(sub.evalElapsedSec));
end
end
